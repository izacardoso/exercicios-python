a = input('digite alguma coisa: ')
print(f'qual o tipo primitivo? {type(a)}')
print(f'esse valor é um numero? {a.isnumeric()}')
print(f'esse valor é alfabetico? {a.isalpha()}')
print(f'esse valor tem espaços? {a.isspace()}')
print(f'esse valor esta em maiusculas? {a.isupper()}')
print(f'esse valor está em minusculas? {a.islower()}')
print(f' esse valor contem numeros e letras? {a.isalnum()}')


